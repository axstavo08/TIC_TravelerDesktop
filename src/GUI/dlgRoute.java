    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String origin = txtOrigin.getText().trim(), destiny = txtDestiny.getText().trim();
        RouteDao rdao = new RouteDaoImpl();
        Boolean action = (txtId.getText().isEmpty()) ? rdao.save(new Route(origin, destiny))
                : rdao.update(Integer.parseInt(txtId.getText()), new Route(origin, destiny));
        if (action) {
            if (txtId.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Ruta Ingresada.", "UTP", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Ruta Modificada.", "UTP", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Error en la acci√≥n.", "UTP", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

