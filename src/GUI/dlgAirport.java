    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String name = txtName.getText().trim(), location = txtLocation.getText().trim();
        Integer capacity = Integer.parseInt(txtCapacity.getText()), tracks = Integer.parseInt(txtTracks.getText());
        AirportDao adao = new AirportDaoImpl();
        Boolean action = (txtId.getText().isEmpty()) ? adao.save(new Airport(name, location, capacity, tracks))
                : adao.update(Integer.parseInt(txtId.getText()), new Airport(name, location, capacity, tracks));
        if (action) {
            if (txtId.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Aeropuerto Ingresada.", "UTP", JOptionPane.INFORMATION_MESSAGE);
                validateCloseWindow(JOptionPane.OK_OPTION);
            } else {
                JOptionPane.showMessageDialog(null, "Aeropuerto Modificada.", "UTP", JOptionPane.INFORMATION_MESSAGE);
                validateCloseWindow(JOptionPane.OK_OPTION);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Error en la acción.", "UTP", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

